package structures

/*
	Интерфейс FileInfo

	type FileInfo interface {
		Name() string       // базовое имя файла
		Size() int64        // размер файла в байтах
		Mode() FileMode     // режим файла
		ModTime() time.Time // время модификации
		IsDir() bool        // возвращает true если это директория
		Sys() interface{}   // базовые данные файловой системы
	}

	FileInfo представляет информацию о файле и является интерфейсом.
	Это означает, что любая структура, которая реализует все методы этого интерфейса,
	может считаться FileInfo.

	Основные методы:

	func Name() string
		- Возвращает базовое имя файла (без пути)
		- Например: для "/home/user/file.txt" вернет "file.txt"

	func Size() int64
		- Возвращает размер файла в байтах
		- Для директорий обычно возвращает 0 или размер метаданных

	func Mode() FileMode
		- Возвращает режим файла (права доступа и тип файла)
		- Включает информацию о правах чтения/записи/выполнения
		- Определяет тип файла (обычный файл, директория, симлинк и т.д.)

	func ModTime() time.Time
		- Возвращает время последней модификации файла
		- Используется для отслеживания изменений файла

	func IsDir() bool
		- Возвращает true если файл является директорией
		- Удобный способ проверить тип файла

	func Sys() interface{}
		- Возвращает базовые данные файловой системы
		- Содержит специфичную для ОС информацию
		- На Unix системах обычно содержит *syscall.Stat_t
*/
